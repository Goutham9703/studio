        function enq_submit(){
        $('.enq-return').empty();
        name = $('.enq-name').val();
        phone = $('.enq-phone').val();
        email = $('.enq-email').val();
        message = $('.enq-message').val();

        dataString = 'name=' + name + '&email=' + email + '&phone=' + phone + '&message=' + message + '&action=contact';
        if(name=='' || email=='' || phone==''){
            $('.enq-return').html('<div class="alert alert-danger">All Fields are required!</div>');
        }else{
            $.ajax({
                url: 'controller.php',
                method: 'post',
                data: dataString,
                cache: false,
                success:function(data){
                    if(data === '1'){
                        window.location.assign("thankyou.php");
                    }else{
                        $('.enq-return').html(data).show();
                    }
                }
            })
        }
    }
